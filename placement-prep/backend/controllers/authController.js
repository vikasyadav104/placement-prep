const updateProfile = async (req, res) => {
  try {
    const { leetcodeUsername, codeforcesUsername } = req.body;

    if (leetcodeUsername !== undefined) req.user.leetcodeUsername = leetcodeUsername;
    if (codeforcesUsername !== undefined) req.user.codeforcesUsername = codeforcesUsername;

    await req.user.save();

    res.status(200).json({ message: 'Profile updated successfully', user: req.user });
  } catch (error) {
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

module.exports = { signup, login, getProfile, updateProfile };